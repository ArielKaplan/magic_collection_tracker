// drop name → superdrop name
const SL_DROP_TO_SUPERDROP = {};
for (const sd of SL_SUPERDROPS) {
  for (const drop of sd.drops) {
    SL_DROP_TO_SUPERDROP[drop] = { superdrop: sd.superdrop, date: sd.date };
  }
}

// card name → array of drops it appears in (may be >1 if reprinted in multiple drops)
const SL_CARD_TO_DROPS = {};
for (const [drop, cards] of Object.entries(SL_DROP_CARDS)) {
  for (const card of cards) {
    if (!SL_CARD_TO_DROPS[card]) SL_CARD_TO_DROPS[card] = [];
    if (!SL_CARD_TO_DROPS[card].includes(drop)) SL_CARD_TO_DROPS[card].push(drop);
  }
}

// Helper: get Secret Lair info for a card name
// Returns array of { drop, superdrop, date } or []
function getSlInfo(cardName) {
  return (SL_CARD_TO_DROPS[cardName] || []).map(drop => {
    const sd = SL_DROP_TO_SUPERDROP[drop] || { superdrop: "Standalone", date: "" };
    return { drop, superdrop: sd.superdrop, date: sd.date };
  });
}
